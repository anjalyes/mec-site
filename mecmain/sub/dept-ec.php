            <h4 class="line"> Association </h4><br>
            
            <p> Mixed Signals, the Electronics Association of Model Engineering College is the joint venture of the staff and students of Electronics and Communication Department. The club aims at making the students aware of the latest cutting edge technologies. </p>
           <p>Mixed Signals, the Electronics Association of Model Engineering College is a joint endeavour of the staffs and students of Electronics Department. With a solid vision ahead, Mixed Signals aims at organising activities that promote interest as well as spread awareness regarding the latest developments in the field of Electronics and Circuit Design, among the members, engineering students and the public.</p> 
           <p>Working steadily towards its mission, the following are some of the activities undertaken by Mixed Signals</p>
              <ul>
                <li>Organising lectures, discussions, seminars and conferences to keep students updated on the recent trends in the electronics industry.</li>
                <li>Holding contests and competitions to test the knowledge of the participants and instil in them the desire to know more.</li>
                <li>Staging exhibitions and demonstrations and conducting workshops and industrial visits to improve the practical knowledge of the budding engineers and make them industry ready.</li>
                <li>Conduct coaching classes for weak students in the specific areas of Electronics and Communication and other subjects on demand.</li>
              </ul>
