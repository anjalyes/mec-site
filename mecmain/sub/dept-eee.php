            <h4 class="deptcont line">Aim</h4><br>
            <p>The department of Electrical Engineering started in the year 2009, offers a vivacious environment for graduate educations in Electrical and Electronic Engineering. It is one of the developed and well equipped departments in the institution. The vision of the department is to provide the leadership to achieve excellence in the field of Electrical Engineering and to create technical manpower of global standards as Electrical Engineering with capabilities of accepting new challenges.</p>
            <p>The department is committed to the advancement of the frontiers of knowledge in Electrical Engineering and to provide the students with a stimulating and rewarding learning experience. The department is dedicated to impart quality and value based education to increase satisfaction level of all stake holders.</p>

            <h4 class="deptcont line">Objectives</h4><br>
            <ul>
              <li>To mould competent Electrical Engineers capable of providing dynamic leadership in various walks of life and to cater the needs of R&D, industry, business and community. </li>
